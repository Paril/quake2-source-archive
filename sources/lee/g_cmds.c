void Cmd_Bot_Teleport (edict_t *ent)
{
	edict_t *bot;
	vec3_t  test;

	//if coop game, get the first bot and teleport the bot
	//as near as possible, also test the new area to see if in the
	//game world
	if (!coop->value)
		return;

	if (current_number_bot == 0)
		return;

	bot = G_Find (NULL, FOFS(classname), "bot");

	if (bot->health < 1)
		return;

	if (bot == NULL)
		return;

	VectorCopy (ent->s.origin, test);
	test[0]+=40;

	if ((gi.pointcontents(test) & CONTENTS_SOLID) != 1)
		{
		gi.WriteByte (svc_muzzleflash);
		gi.WriteShort (ent-g_edicts);
		gi.WriteByte (MZ_LOGIN);
		gi.multicast (bot->s.origin, MULTICAST_PVS);
		VectorCopy(test, bot->s.origin);		
		if (strcmp(ent->classname, "player") == 0)
			gi.cprintf (ent, PRINT_HIGH, "Bot teleported\n");
		return;
		}
	 
	test[0]-=80;

	if ((gi.pointcontents(test) & CONTENTS_SOLID) != 1)
		{
		gi.WriteByte (svc_muzzleflash);
		gi.WriteShort (ent-g_edicts);
		gi.WriteByte (MZ_LOGIN);
		gi.multicast (bot->s.origin, MULTICAST_PVS);
		VectorCopy(test, bot->s.origin);
		if (strcmp(ent->classname, "player") == 0)
			gi.cprintf (ent, PRINT_HIGH, "Bot teleported\n");
		return;
		}

	if (strcmp(ent->classname, "player") == 0)
		gi.cprintf (ent, PRINT_HIGH, "Can not find free space for bot\n");
}

char *ClientTeam (edict_t *ent)
{
	char		*p;
	static char	value[512];

	value[0] = 0;

	if (!ent->client)
		return value;

	strcpy(value, Info_ValueForKey (ent->client->pers.userinfo, "skin"));
	p = strchr(value, '/');
	if (!p)
		return value;

	if ((int)(dmflags->value) & DF_MODELTEAMS)
	{
		*p = 0;
		return value;
	}

	// if ((int)(dmflags->value) & DF_SKINTEAMS)
	return ++p;
}


qboolean OnSameTeam (edict_t *ent1, edict_t *ent2)
{
	char	ent1Team [512];
	char	ent2Team [512];

	if (ctf->value)			
		if (CheckTeamDamage (ent1,ent2))
			return true;
		else
			return false;

	if (!((int)(dmflags->value) & (DF_MODELTEAMS | DF_SKINTEAMS)))
		return false;

	strcpy (ent1Team, ClientTeam (ent1));
	strcpy (ent2Team, ClientTeam (ent2));
	
	if (strcmp(ent1Team, ent2Team) == 0)
		return true;
	return false;
}


void SelectNextItem (edict_t *ent, int itflags)
{
	gclient_t	*cl;
	int			i, index;
	gitem_t		*it;

	cl = ent->client;

	//ctf
	if (cl->menu) 
	{
		PMenu_Next(ent);
		return;
	}

	// scan  for the next valid one
	for (i=1 ; i<=MAX_ITEMS ; i++)
	{
		index = (cl->pers.selected_item + i)%MAX_ITEMS;
		if (!cl->pers.inventory[index])
			continue;
		it = &itemlist[index];
		if (!it->use)
			continue;
		if (!(it->flags & itflags))
			continue;

		cl->pers.selected_item = index;
		return;
	}

	cl->pers.selected_item = -1;
}

