/*
==================
Cmd_Use_f

Use an inventory item
==================
*/
void Cmd_Use_f (edict_t *ent)
{
	int			index;
	gitem_t		*it;
	char		*s, *s2;

	s = gi.args();
	it = FindItem (s);
	if (!it)
	{
		if (strcmp(ent->classname, "player") == 0)
			gi.cprintf (ent, PRINT_HIGH, "unknown item: %s\n", s);
		return;
	}
	if (!it->use)
	{
		if (strcmp(ent->classname, "player") == 0)
			gi.cprintf (ent, PRINT_HIGH, "Item is not usable.\n");
		return;
	}
	index = ITEM_INDEX(it);
	if (!ent->client->pers.inventory[index])
	{
		if (strcmp(ent->classname, "player") == 0)
			gi.cprintf (ent, PRINT_HIGH, "Out of item: %s\n", s);
		return;
	}

	s2 = ent->client->pers.weapon->pickup_name;	

	//change grenade type (prox or normal)
	if ((strcmp(s2,"Grenades") == 0) && (strcmp(s, "grenades") == 0))
	{
		if (ent->grenade_type)
		{
			if (strcmp(ent->classname, "player") == 0)
			gi.cprintf (ent, PRINT_HIGH, "Normal grenades selected.\n");
			ent->grenade_type = false;
		}
		else
		{
			ent->grenade_type = true;
			if (strcmp(ent->classname, "player") == 0)
			gi.cprintf (ent, PRINT_HIGH, "Proximity grenades selected.\n");
		}
		
	}
	//not a grenade weapon
	else if ((strcmp(s2, s)) && (strcmp(ent->classname, "bot") != 0))
	{
		if (strcmp(ent->classname, "player") == 0)
			gi.cprintf (ent, PRINT_HIGH, "%s selected.\n", s);
		ent->grenade_type = false;
	}

	it->use (ent, it);
}


