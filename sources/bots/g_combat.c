void teamplay_TDamage (edict_t *targ, edict_t *inflictor, edict_t *attacker, vec3_t dir, vec3_t point, vec3_t normal, int damage, int knockback, int dflags, int mod)
{
	edict_t		*player = NULL;
	gclient_t	*client;
	qboolean	sameteam, lb_uncloak;
	int			take, li_radius, li_mod;
	int			save;
	int			asave;
	int			psave;
	int			te_sparks;

	if (!targ->takedamage)
		return;

	li_mod = mod;

	if (teamplay)
	{
		if ((mod == MOD_APOCSUICIDE) || (mod == MOD_TELEFRAG))
			sameteam = false;
		else
			sameteam = teamplay_CheckTeamDamage(targ, attacker);

		if (sameteam)
			mod |= MOD_FRIENDLY_FIRE;
	}
	else if ((targ != attacker) && ((deathmatch->value && ((int)(dmflags->value) & (DF_MODELTEAMS | DF_SKINTEAMS))) || coop->value))
	{
		if (OnSameTeam (targ, attacker))
		{
			if ((int)(dmflags->value) & DF_NO_FRIENDLY_FIRE)
				damage = 0;
			else
				mod |= MOD_FRIENDLY_FIRE;
		}
	}
	meansOfDeath = mod;

	// easy mode takes half damage
	if (skill->value == 0 && deathmatch->value == 0 && targ->client)
	{
		damage *= 0.5;
		if (!damage)
			damage = 1;
	}

	// ### Hentai ### BEGIN Teamplay
	if(!(dflags & DAMAGE_RADIUS) && (dir) && (point))
	{
		vec3_t org, offset, dir1, dir2;
		float hitoffset, hitfactor, movelen;
	
		org[0] = targ->s.origin[0];
		org[1] = targ->s.origin[1];
		org[2] = targ->s.origin[2] + targ->viewheight;
		
		VectorSubtract(point, org, offset);

		VectorCopy(dir, dir1);
		VectorNormalize(dir1);
		movelen = DotProduct(offset, dir1);
		VectorMA (offset, -movelen, dir1, dir2);
		dir2[0] = dir2[0] * 2;
		dir2[1] = dir2[1] * 2;
		hitoffset = DotProduct(dir2, dir2);
		hitfactor = DotProduct(targ->size, targ->size) / (hitoffset * 2 + DotProduct(targ->size, targ->size) * 0.5);
		//gi.dprintf ("Damage multiplier: %f\n", hitfactor);
		damage *= hitfactor;
		if (!damage)
			damage = 1;
	}
	// ### Hentai ### END Teamplay

	client = targ->client;

	//BEGIN Teamplay
	if ((client) && (targ->health > 0))
	{
		if (!IsFemale(targ))
			li_radius = 300;
		else
			li_radius = 500;

		if (teams.hasprotect[targ->client->pers.team-1])
		{
			while ((player = findradius(player, targ->s.origin, li_radius)) != NULL)
			{
				if (!(player->svflags & SVF_MONSTER) && !player->client)
					continue;
				if (player == targ)
					continue;
				if (player->client->observer)
					continue;
				if (!player->takedamage)
					continue;
				if (player->health <= 0)
					continue;
				if (targ->client->pers.team != player->client->pers.team)
					continue;
				if (player->client->pers.player_class != 2)
					continue;
				if (player->client->lifelink == false)
					continue;

				gi.bprintf (PRINT_MEDIUM,"Bodyguard %s is protecting %s!\n", player->client->pers.netname, targ->client->pers.netname);
				client = player->client;
				targ = player;
				damage = (int)(damage / client->pers.classlevel);

				break;
			}
		}
	}

	if (client)
	{
		if (client->invisible)
		{
			lb_uncloak = false;
			if (damage > (client->pers.classlevel * 20))
				lb_uncloak = true;

			if (lb_uncloak)
			{
				client->invisible = false;
				client->last_movement = level.time;
				gsutil_centerprint(targ, "%s", "You are visible again!\n");
				targ->s.modelindex = 255;
			}
		}
	}
	//END Teamplay

	if (dflags & DAMAGE_BULLET)
		te_sparks = TE_BULLET_SPARKS;
	else
		te_sparks = TE_SPARKS;

	VectorNormalize(dir);

// bonus damage for suprising a monster
	if (!(dflags & DAMAGE_RADIUS) && (targ->svflags & SVF_MONSTER) && (attacker->client) && (!targ->enemy) && (targ->health > 0))
		damage *= 2;

	//BEGIN Teamplay
	if((teamplay) && ((int)(teamflags->value) & TEAM_ATTACKER_DAMAGE) && sameteam)
		targ = attacker;
	//END Teamplay

	if (targ->flags & FL_NO_KNOCKBACK)
		knockback = 0;

// figure momentum add
	if (!(dflags & DAMAGE_NO_KNOCKBACK))
	{
		if ((knockback) && (targ->movetype != MOVETYPE_NONE) && (targ->movetype != MOVETYPE_BOUNCE) && (targ->movetype != MOVETYPE_PUSH) && (targ->movetype != MOVETYPE_STOP))
		{
			vec3_t	kvel;
			float	mass;

			if (targ->mass < 50)
				mass = 50;
			else
				mass = targ->mass;

			if (targ->client  && attacker == targ)
				VectorScale (dir, 1600.0 * (float)knockback / mass, kvel);	// the rocket jump hack...
			else
				VectorScale (dir, 500.0 * (float)knockback / mass, kvel);

			VectorAdd (targ->velocity, kvel, targ->velocity);
		}
	}

	take = damage;
	save = 0;

	// check for godmode
	if ( (targ->flags & FL_GODMODE) && !(dflags & DAMAGE_NO_PROTECTION) )
	{
		take = 0;
		save = damage;
		SpawnDamage (te_sparks, point, normal, save);
	}

	// check for invincibility
	if (client)
	{
		if ((client->invincible_framenum > level.framenum ) && !(dflags & DAMAGE_NO_PROTECTION))
		{
			if (targ->pain_debounce_time < level.time)
			{
				gi.sound(targ, CHAN_ITEM, gi.soundindex("items/protect4.wav"), 1, ATTN_NORM, 0);
				targ->pain_debounce_time = level.time + 2;
			}
			take = 0;
			save = damage;
		}
	}

	// BEGIN Teamplay
	if((targ == attacker) && ((int)(teamflags->value) & TEAM_NOSPLASH_DAMAGE))
		return;
	else if (!(teamplay) || !((int)(teamflags->value) & TEAM_ARMOR_PROTECT && sameteam))
	{
		psave = CheckPowerArmor (targ, point, normal, take, dflags);
		take -= psave;

		asave = CheckArmor (targ, point, normal, take, te_sparks, dflags);
		take -= asave;

		//treat cheat/powerup savings the same as armor
		asave += save;
	}

	// team damage avoidance
	if ((teamplay) && ((int)(teamflags->value) & TEAM_HEALTH_PROTECT) && sameteam && (targ != attacker))
		return;
	else if (!(dflags & DAMAGE_NO_PROTECTION) && CheckTeamDamage (targ, attacker))
		return;
	// END Teamplay

// do the damage
	if (take)
	{
		if ((targ->svflags & SVF_MONSTER) || (client))
			SpawnDamage (TE_BLOOD, point, normal, take);
		else
			SpawnDamage (te_sparks, point, normal, take);


		// BEGIN Teamplay
		if ((li_mod == MOD_R_SPLASH) && (targ == attacker))
		{
			if (client)
			{
				if (client->pers.classlevel < 3)
					targ->health = targ->health - take;
			}
			else
				targ->health = targ->health - take;
		}
		else if ((li_mod == MOD_G_SPLASH) && (targ == attacker))
		{
			if (!client)
				targ->health = targ->health - take;
		}
		else
			targ->health = targ->health - take;
		// END Teamplay
			
		if (targ->health <= 0)
		{
			if ((targ->svflags & SVF_MONSTER) || (client))
				targ->flags |= FL_NO_KNOCKBACK;
			Killed (targ, inflictor, attacker, take, point);

			// BEGIN Teamplay
			if((teamplay) && ((int)(teamflags->value) & TEAM_DEATH_PENALTY) && teamplay_CheckTeamDamage(targ, attacker))
			{
				Killed(attacker, inflictor, attacker, take*1000, point);
				attacker->client->newweapon = NULL;
				ChangeWeapon (attacker);
			}
			// END Teamplay

			return;
		}
	}

	if (targ->svflags & SVF_MONSTER)
	{
		M_ReactToDamage (targ, attacker);
		if (!(targ->monsterinfo.aiflags & AI_DUCKED) && (take))
		{
			targ->pain (targ, attacker, knockback, take);
			// nightmare mode monsters don't go into pain frames often
			if (skill->value == 3)
				targ->pain_debounce_time = level.time + 5;
		}
	}
	else if (client)
	{
		if (!(targ->flags & FL_GODMODE) && (take))
			targ->pain (targ, attacker, knockback, take);
	}
	else if (take)
	{
		if (targ->pain)
			targ->pain (targ, attacker, knockback, take);
	}

	// add to the damage inflicted on a player this frame
	// the total will be turned into screen blends and view angle kicks
	// at the end of the frame
	if (client)
	{
		client->damage_parmor += psave;
		client->damage_armor += asave;
		client->damage_blood += take;
		client->damage_knockback += knockback;
		VectorCopy (point, client->damage_from);
	}
}
