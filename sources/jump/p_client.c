/*
===========
ClientUserInfoChanged

called whenever the player updates a userinfo variable.

The game can override any of the settings in place
(forcing skins or names, etc) before copying it off.
============
*/
void ClientUserinfoChanged (edict_t *ent, char *userinfo)
{
	int		nasty = 0, need_configstrings = 0;
	int		i;
	char	*s;
	int		playernum;

	// check for malformed or illegal info strings
	if (!Info_Validate(userinfo))
	{
		strcpy (userinfo, "\\name\\badinfo\\skin\\male/grunt");
	}

	playernum = ent-g_edicts-1;

	// set name
	s = Info_ValueForKey (userinfo, "name");
	if (Q_stricmp (s, ent->client->pers.netname)) {
		strncpy (ent->client->pers.netname, s, sizeof(ent->client->pers.netname)-1);
		gi.configstring (CS_GENERAL+playernum, va("%s", ent->client->pers.netname));
		nasty = 1;
		need_configstrings = 1;
	}

	if (sv_fpscap->value) {
		s = Info_ValueForKey (userinfo, "cl_maxfps");
		if (ent->inuse && *s && atoi(s) && atoi(s) > (int)sv_fpscap->value && Q_stricmp (ent->classname, "kicked")) {
			gi.bprintf (PRINT_HIGH, "%s was kicked for high FPS!\n", ent->client->pers.netname);
			gi.AddCommandString (va("kick %d\n", playernum));
			ent->classname = "kicked";
			return;
		}
	}

	// set spectator
	s = Info_ValueForKey (userinfo, "spectator");
	// spectators are only supported in deathmatch
	if (deathmatch->value && *s && strcmp(s, "0"))
		ent->client->pers.spectator = true;
	else
		ent->client->pers.spectator = false;

	// set skin
	s = Info_ValueForKey (userinfo, "skin");

	// combine name and skin into a configstring
	if (Q_stricmp (s, ent->client->pers.skin) || need_configstrings) {
		strncpy (ent->client->pers.skin, s, sizeof(ent->client->pers.skin)-1);
		gi.configstring (CS_PLAYERSKINS+playernum, va("%s\\%s", ent->client->pers.netname, s) );
	}

	// fov
	if (deathmatch->value && ((int)dmflags->value & DF_FIXED_FOV))
	{
		ent->client->ps.fov = 90;
	}
	else
	{
		ent->client->ps.fov = atoi(Info_ValueForKey(userinfo, "fov"));
		if (ent->client->ps.fov < 1)
			ent->client->ps.fov = 90;
		else if (ent->client->ps.fov > 160)
			ent->client->ps.fov = 160;
	}

	// handedness
	s = Info_ValueForKey (userinfo, "hand");
	if (strlen(s))
	{
		ent->client->pers.hand = atoi(s);
	}

	if (ent->inuse && nasty) {
		i = ent->client->userflood_whenhead + 1;
		if (i > sizeof(ent->client->userflood_when)/sizeof(ent->client->userflood_when[0]))
			i = 0;

		if (ent->client->userflood_when[i] && level.time - ent->client->userflood_when[i] < 4) {
			gi.bprintf (PRINT_HIGH, "%s was kicked for userinfo flood!\n", ent->client->pers.netname);
			gi.AddCommandString (va("kick %d\n", playernum));
			return;
		}

		ent->client->userflood_whenhead = (ent->client->userflood_whenhead + 1) % (sizeof(ent->client->userflood_when)/sizeof(ent->client->userflood_when[0]));
		ent->client->userflood_when[ent->client->userflood_whenhead] = level.time;
	}
	// save off the userinfo in case we want to check something later
	strncpy (ent->client->pers.userinfo, userinfo, sizeof(ent->client->pers.userinfo)-1);
}


/*
===========
ClientConnect

Called when a player begins connecting to the server.
The game can refuse entrance to a client by returning false.
If the client is allowed, the connection process will continue
and eventually get to ClientBegin()
Changing levels will NOT cause this to be called again, but
loadgames will.
============
*/
qboolean ClientConnect (edict_t *ent, char *userinfo)
{
	char	*value;
	char	*ip;

	// check to see if they are on the banned IP list
	//value = Info_ValueForKey (userinfo, "ip");
	ip = strstr(userinfo,"\\ip\\");

	if (!ip)
		return false;

	ip +=4;

	if (!*ip || SV_FilterPacket(ip)) {
		Info_SetValueForKey(userinfo, "rejmsg", "Banned.");
		return false;
	}

	// check for a spectator
	value = Info_ValueForKey (userinfo, "spectator");
	if (deathmatch->value && *value && strcmp(value, "0")) {
		int i, numspec;

		if (*spectator_password->string && 
			strcmp(spectator_password->string, "none") && 
			strcmp(spectator_password->string, value)) {
			Info_SetValueForKey(userinfo, "rejmsg", "Spectator password required or incorrect.");
			return false;
		}

		// count spectators
		for (i = numspec = 0; i < maxclients->value; i++)
			if (g_edicts[i+1].inuse && g_edicts[i+1].client->pers.spectator)
				numspec++;

		if (numspec >= maxspectators->value) {
			Info_SetValueForKey(userinfo, "rejmsg", "Server spectator limit is full.");
			return false;
		}
	} else {
		// check for a password
		value = Info_ValueForKey (userinfo, "password");
		if (*password->string && strcmp(password->string, "none") && 
			strcmp(password->string, value)) {
			Info_SetValueForKey(userinfo, "rejmsg", "Password required or incorrect.");
			return false;
		}
	}

	// they can connect
	ent->client = game.clients + (ent - g_edicts - 1);

	//r1: server didn't drop the old client properly.
	if (ent->classname) {
		ent->classname = NULL;
		memset (&ent->client->pers, 0, sizeof(ent->client->pers));
	}

	InitClientResp (ent->client);
	InitClientPersistant (ent->client);

	memset (ent->client->userflood_when, 0, sizeof(ent->client->userflood_when));
	ClientUserinfoChanged (ent, userinfo);

	if (game.maxclients > 1)
		gi.dprintf ("%s connected\n", ent->client->pers.netname);

	ent->svflags = 0; // make sure we start with known default
	ent->client->pers.connected = true;
	return true;
}

/*
===========
ClientDisconnect

Called when a player drops from the server.
Will not be called between levels.
============
*/
void ClientDisconnect (edict_t *ent)
{
	edict_t	*e;
	int		playernum;

	if (!ent->client)
		return;

	if (ent->inuse && ent->client->resp.enterframe)
		gi.bprintf (PRINT_HIGH, "%s disconnected\n", ent->client->pers.netname);

	gi.unlinkentity (ent);

	ent->s.modelindex = 0;
	ent->solid = SOLID_NOT;

	ent->inuse = false;
	ent->classname = NULL;
	ent->client->pers.connected = false;

	for (e = g_edicts + 1; e < &g_edicts[game.maxclients +1]; e++) {
		if (e->inuse && e->client->chase_target == ent)
			UpdateChaseCam (e);
	}

	ent->client->resp.checkpoint = 0;
	ent->client->resp.enterframe = 0;

	playernum = ent-g_edicts-1;

	gi.configstring (CS_PLAYERSKINS+playernum, "");
	//gi.configstring (CS_GENERAL+playernum, "");
}


