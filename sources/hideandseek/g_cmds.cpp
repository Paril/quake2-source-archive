/*
=================
Cmd_Players_f
=================
*/
void Cmd_Players_f (edict_t *ent)
{
	int		i;
	int		count;
	char	small[64];
	char	large[1280];
	int		index[256];

	count = 0;
	for (i = 0 ; i < maxclients->floatVal ; i++)
		if (game.clients[i].pers.connected)
		{
			index[count] = i;
			count++;
		}


	// print information
	large[0] = 0;

	for (i = 0 ; i < count ; i++)
	{
		Q_snprintfz (small, sizeof(small), "%s\n",
			game.clients[index[i]].pers.netname);
		if (strlen (small) + strlen(large) > sizeof(large) - 100 )
		{	// can't print all of them in one packet
			strcat (large, "...\n");
			break;
		}
		strcat (large, small);
	}

	gi.cprintf (ent, PRINT_HIGH, "%s\n%i players\n", large, count);
}

/*
=================
Cmd_Wave_f
=================
*/
void Cmd_Wave_f (edict_t *ent)
{
	int		i;

	i = atoi (gi.argv(1));

	// can't wave when ducked
	if (ent->client->ps.pMove.pmFlags & PMF_DUCKED)
		return;

	if (ent->client->anim_priority > ANIM_WAVE)
		return;

	ent->client->anim_priority = ANIM_WAVE;

	switch (i)
	{
	case 0:
		gi.cprintf (ent, PRINT_HIGH, "flipoff\n");
		ent->s.frame = FRAME_flip01-1;
		ent->client->anim_end = FRAME_flip12;
		break;
	case 1:
		gi.cprintf (ent, PRINT_HIGH, "salute\n");
		ent->s.frame = FRAME_salute01-1;
		ent->client->anim_end = FRAME_salute11;
		break;
	case 2:
		gi.cprintf (ent, PRINT_HIGH, "taunt\n");
		ent->s.frame = FRAME_taunt01-1;
		ent->client->anim_end = FRAME_taunt17;
		break;
	case 3:
		gi.cprintf (ent, PRINT_HIGH, "wave\n");
		ent->s.frame = FRAME_wave01-1;
		ent->client->anim_end = FRAME_wave11;
		break;
	case 4:
	default:
		gi.cprintf (ent, PRINT_HIGH, "point\n");
		ent->s.frame = FRAME_point01-1;
		ent->client->anim_end = FRAME_point12;
		break;
	}
}

/*
==================
Cmd_Say_f
==================
*/
void Cmd_Say_f (edict_t *ent, bool team, bool arg0)
{
	int		i, j;
	edict_t	*other;
	char	*p;
	char	text[2048];
	gclient_t *cl;

	if (gi.argc () < 2 && !arg0)
		return;

	if (!((int)(dmflags->floatVal) & (DF_MODELTEAMS | DF_SKINTEAMS)))
		team = false;

	if (team)
		Q_snprintfz (text, sizeof(text), "(%s): ", ent->client->pers.netname);
	else
		Q_snprintfz (text, sizeof(text), "%s: ", ent->client->pers.netname);

	if (arg0)
	{
		strcat (text, gi.argv(0));
		strcat (text, " ");
		strcat (text, gi.args());
	}
	else
	{
		p = gi.args();

		if (*p == '"')
		{
			p++;
			p[strlen(p)-1] = 0;
		}
		strcat(text, p);
	}

	// don't let text be too long for malicious reasons
	if (strlen(text) > 150)
		text[150] = 0;

	strcat(text, "\n");

	if (flood_msgs->floatVal) {
		cl = ent->client;

        if (level.time < cl->flood_locktill) {
			gi.cprintf(ent, PRINT_HIGH, "You can't talk for %d more seconds\n",
				(int)(cl->flood_locktill - level.time));
            return;
        }
        i = cl->flood_whenhead - flood_msgs->floatVal + 1;
        if (i < 0)
            i = (sizeof(cl->flood_when)/sizeof(cl->flood_when[0])) + i;
		if (cl->flood_when[i] && 
			level.time - cl->flood_when[i] < flood_persecond->floatVal) {
			cl->flood_locktill = level.time + flood_waitdelay->floatVal;
			gi.cprintf(ent, PRINT_CHAT, "Flood protection:  You can't talk for %d seconds.\n",
				(int)flood_waitdelay->floatVal);
            return;
        }
		cl->flood_whenhead = (cl->flood_whenhead + 1) %
			(sizeof(cl->flood_when)/sizeof(cl->flood_when[0]));
		cl->flood_when[cl->flood_whenhead] = level.time;
	}

	if (dedicated->floatVal)
		gi.cprintf(NULL, PRINT_CHAT, "%s", text);

	for (j = 1; j <= game.maxclients; j++)
	{
		other = &g_edicts[j];
		if (!other->inUse)
			continue;
		if (!other->client)
			continue;
		if (team)
		{
			if (!OnSameTeam(ent, other))
				continue;
		}
		gi.cprintf(other, PRINT_CHAT, "%s", text);
	}
}

void Cmd_PlayerList_f(edict_t *ent)
{
	int i;
	char st[80];
	char text[1400];
	edict_t *e2;

	// connect time, ping, score, name
	*text = 0;
	for (i = 0, e2 = g_edicts + 1; i < maxclients->floatVal; i++, e2++) {
		if (!e2->inUse)
			continue;

		Q_snprintfz(st, sizeof(st), "%02d:%02d %4d %3d %s%s\n",
			(level.framenum - e2->client->resp.enterframe) / 600,
			((level.framenum - e2->client->resp.enterframe) % 600)/10,
			e2->client->ping,
			e2->client->resp.score,
			e2->client->pers.netname,
			e2->client->resp.spectator ? " (spectator)" : "");
		if (strlen(text) + strlen(st) > sizeof(text) - 50) {
			sprintf(text+strlen(text), "And more...\n");
			gi.cprintf(ent, PRINT_HIGH, "%s", text);
			return;
		}
		strcat(text, st);
	}
	gi.cprintf(ent, PRINT_HIGH, "%s", text);
}

void Get_Origin (edict_t *player, vec3_t out)
{
	vec3_t offset, start, forward, right, up, end;
	cmTrace_t tr;

	Angles_Vectors (player->client->v_angle, forward, right, up);

	Vec3Set(offset,0 , 0, player->viewheight-7);
	G_ProjectSource (player->s.origin, offset, forward, right, start);
	Vec3MA(start,8192,forward,end);

	tr = gi.trace (start, NULL, NULL, end, player, CONTENTS_MASK_SOLID);

	Vec3Copy (tr.endPos, out);
}

void followCursor (edict_t *ent)
{
	Get_Origin(ent->owner, ent->s.origin);
	ent->nextthink = level.time + .1;
	gi.linkentity(ent);
}

void FlashLight (edict_t *ent)
{
	edict_t *fl;
	if (ent->client->resp.flashlight)
	{
		G_FreeEdict(ent->client->resp.flashlight);
		ent->client->resp.flashlight = NULL;
		return;
	}

	fl = G_Spawn();

	Get_Origin (ent, fl->s.origin);
	fl->s.modelIndex = gi.modelindex("models/objects/grenade2/tris.md2");

	fl->movetype = MOVETYPE_NONE;
	fl->think = followCursor;
	fl->nextthink = level.time + .1;
	fl->s.effects = EF_PLASMA;
	fl->owner = ent;

	gi.linkentity(fl);

	ent->client->resp.flashlight = fl;
}

/*
=================
ClientCommand
=================
*/
void Cmd_Hook_f(edict_t *ent, char *cmd);
void ClientCommand (edict_t *ent)
{
	char	*cmd;

	if (!ent->client)
		return;		// not fully in game yet

	cmd = gi.argv(0);

	if (Q_stricmp (cmd, "players") == 0)
	{
		Cmd_Players_f (ent);
		return;
	}
	if (Q_stricmp (cmd, "say") == 0)
	{
		Cmd_Say_f (ent, false, false);
		return;
	}
	if (Q_stricmp (cmd, "say_team") == 0)
	{
		Cmd_Say_f (ent, true, false);
		return;
	}
	if (Q_stricmp (cmd, "score") == 0)
	{
		Cmd_Score_f (ent);
		return;
	}
	if (Q_stricmp (cmd, "help") == 0)
	{
		Cmd_Help_f (ent);
		return;
	}

	if (level.intermissiontime)
		return;

	if (Q_stricmp (cmd, "god") == 0)
		Cmd_God_f (ent);
	else if (Q_stricmp (cmd, "notarget") == 0)
		Cmd_Notarget_f (ent);
	else if (Q_stricmp (cmd, "noclip") == 0)
		Cmd_Noclip_f (ent);
	else if (Q_stricmp (cmd, "kill") == 0)
		Cmd_Kill_f (ent);
	else if (Q_stricmp (cmd, "putaway") == 0)
		Cmd_PutAway_f (ent);
	else if (Q_stricmp (cmd, "wave") == 0)
		Cmd_Wave_f (ent);
	else if (Q_stricmp(cmd, "playerlist") == 0)
		Cmd_PlayerList_f(ent);
	else if (Q_stricmp(cmd, "hiding") == 0)
	{
		if (level.hider == ent && level.seeker && !level.hiding)
		{
			float up = level.hider->s.origin[2] - level.seeker->s.origin[2];
			level.hiding = true;
			gi.cprintf (level.seeker, PRINT_CHAT, "== Hider has hid!\nHint: He is %s you :)\n", up > 24 ? "above" : up < -24 ? "below" : "on the same level");
		}
	}
	else if ((Q_stricmp(cmd, "hook") == 0)
		|| (Q_stricmp(cmd, "hook2") == 0))
		Cmd_Hook_f(ent,cmd); 
	else if (Q_stricmp(cmd, "hint") == 0)
	{
		if (!level.seeker || !level.hider || !level.hiding)
			return;
		gi.cprintf (level.seeker, PRINT_CHAT, "Hint requested!\n");
		gi.cprintf (level.hider, PRINT_CHAT, "The hider requested a hint!\n");

		gi.sound (level.hider, CHAN_AUTO, gi.soundindex("world/brkglas.wav"), 1, ATTN_NORM, 0);
	}
	else if (Q_stricmp(cmd, "flashlight") == 0)
		FlashLight(ent);
	else	// anything that doesn't match a command will be a chat
		Cmd_Say_f (ent, false, true);
}
